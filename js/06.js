// Objeto Math

// se imprime el objeto Math
console.log(window.Math); 
// se imprime el valor de PI
console.log('window.Math.PI: ' + window.Math.PI);
// redondea un numero ya sea hacia o hacia abajo
console.log('window.Math.round(2.5): ' + window.Math.round(2.5));
// siempre redondea un numero hacia el siguiente numero mayor entero
console.log('window.Math.ceil(2.1): ' + window.Math.ceil(2.1));
// siempre redondea un numero hacia el anterior numero menor entero
console.log('window.Math.floor(2.9): ' + window.Math.floor(2.9));
// calcula la raiz cuadrada de un n√∫mero
console.log('window.Math.sqrt(16): ' + window.Math.sqrt(16));
// retorna el valor absoluto de cualquier numero
console.log('window.Math.abs(-2.5): ' + window.Math.abs(-2.5));
// retorna el menor numero de un arreglo de enteros
console.log('window.Math.min(502, 85, 68, 978, 1, -85): ' + window.Math.min(502, 85, 68, 978, 1, -85));
// retorna el mayor numero de un arreglo de enteros
console.log('window.Math.max(502, 85, 68, 978, 1, -85): ' + window.Math.max(502, 85, 68, 978, 1, -85));
// retorna un numero aleatrio
console.log('window.Math.random(): ' + window.Math.random());
// retorna un numero aleatrio entero
console.log('window.Math.random: ' + window.Math.floor(Math.random() * 30));
